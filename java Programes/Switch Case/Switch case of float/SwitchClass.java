package WrapperClasses;

public class SwitchClass {

    public static void main(String[]args){
      float number=3.0f;

     switch (number){
         case 3.0f:
             System.out.println("3.0");
             break;
         case 3.00f:
             System.out.println("3.00");
             break;
         case 3.000f:
             System.out.println("3.000");
             break;
         case 3.0000f:
             System.out.println("3.0000");
             break;
         case 3.00000f:
             System.out.println("3.00000");
             break;
     }

    }




}
