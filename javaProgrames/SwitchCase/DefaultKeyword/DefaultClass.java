public class DefaultClass {

    public static void main(String[] args) {
        int number=30;

        switch (number){
            default:
                System.out.println("case 10 , case 20 & case 25 are not match to case 30 ");
                break;
            case 30:
                System.out.println("30");
            break;
            case 10:
                System.out.println(30);
                break;
            case 20:
                System.out.println(20);
                break;

            case 25:
                System.out.println(25);




        }
    }
}
