public class RecordOfStudent {
    public String name;
    public int marks;

    public RecordOfStudent(){

    }

    public RecordOfStudent(String name, int marks){
        this.name=name;
        this.marks=marks;

    }

    //getter


    public String getName() {
        return name;
    }

    public int getMarks() {
        return marks;
    }
}
